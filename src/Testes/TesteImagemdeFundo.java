    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (!txtCaminho.getText().trim().equals("")) {
            // Escreve a imagem gerada para um arquivo
            try {     
                BufferedImage image = ImageIO.read(Nomeimagem);
//                image.getScaledInstance(this.getWidth(), this.getHeight(), 10000);
                // Salva como JPEG
                File file = new File("Imagem.jpg");
                ImageIO.write( image, "JPG", file);
            } catch (java.io.IOException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
}//GEN-LAST:event_btnOkActionPerformed

    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        javax.swing.JFileChooser jfc = new javax.swing.JFileChooser(new File("FotodosLeitores/"), FileSystemView.getFileSystemView());
        jfc.setMultiSelectionEnabled(false);
        jfc.setDialogTitle("Selecione a foto do leitor");
        jfc.setFileFilter(new FileNameExtensionFilter("JPG, GIF e PNG", "jpg", "gif", "png"));
        //      this.lblFoto.setText("");
        jfc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);// ha outras opcoes alem de files_only
        int resposta = jfc.showOpenDialog(this);
        if (resposta == 0) {
            Nomeimagem = jfc.getSelectedFile();
            // se clica em carregar imagem faz isso...
 img = new ImageIcon(jfc.getSelectedFile().getAbsolutePath()).getImage().getScaledInstance(this.getWidth(),this.getHeight(),10000);
//            img = new ImageIcon(jfc.getSelectedFile().getAbsolutePath()).getImage();
            txtCaminho.setText(Nomeimagem.getAbsolutePath());
        }
}//GEN-LAST:event_btnProcurarActionPerformed

    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
//frmMenuTESTE menu= new frmMenuTESTE();
        if (Nomeimagem != null && Nomeimagem.exists()) {
            lblImagem.setIcon(new ImageIcon(img.getScaledInstance(this.getWidth(), this.getHeight(), Image.SCALE_DEFAULT)));
            lblImagem.setVisible(true);
            lblImagem.setSize(this.getSize());
            this.getContentPane().add(lblImagem);
            this.repaint();
//        menu.caminhoImagemFundo=txtCaminho.getText().trim();
//        menu.visualizacao=true;
        }
        else if (Nomeimagem == null) {
            JOptionPane.showMessageDialog(null, "Selecione uma imagem", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else if (Nomeimagem.exists() == false) {
            JOptionPane.showMessageDialog(null, "A imagem n√£o existe", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnVisualizarActionPerformed

