            tblLeitores.getColumnModel().getColumn(1).setPreferredWidth(100);
            tblLeitores.getColumnModel().getColumn(2).setPreferredWidth(100);
            tblLeitores.getColumnModel().getColumn(3).setPreferredWidth(350);

