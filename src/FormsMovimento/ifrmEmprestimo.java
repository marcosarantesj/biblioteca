    private void btnIncluirReservasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirReservasActionPerformed

        //Se o modelo não foi inicializado, então inicializa
        if (modelObra == null) {
            //Percorre a tabela de reservas
            for (int i = 0; i
                    < tblReservas.getRowCount(); i++) {
                dadosObra.add(new String[]{tblReservas.getValueAt(i, 0).toString(),
                            tblReservas.getValueAt(i, 1).toString(),
                            tblReservas.getValueAt(i, 2).toString()});
            }
            //------------------------------------------------------------------
            boolean[] edicao = {false, false, false};
            modelObra = new MkTableModel(dadosObra, colunasObra, edicao);
            tblEmprestimos.setModel(modelObra);
            tblEmprestimos.getColumnModel().getColumn(0).setPreferredWidth(120);
            tblEmprestimos.getColumnModel().getColumn(1).setPreferredWidth(350);
            tblEmprestimos.getColumnModel().getColumn(2).setPreferredWidth(100);
            btnIncluirReservas.setEnabled(false);
        } else {
            //Percorre a tabela de reservas
            for (int i = 0; i
                    < tblReservas.getRowCount(); i++) {
                modelObra.addRow(new String[]{tblReservas.getValueAt(i, 0).toString(),
                            tblReservas.getValueAt(i, 1).toString(),
                            tblReservas.getValueAt(i, 2).toString()});
            }
            btnIncluirReservas.setEnabled(false);
        }

    }//GEN-LAST:event_btnIncluirReservasActionPerformed

