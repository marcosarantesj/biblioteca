    private void carregaObrasdoLeitor() {
        banco2 = new DB();
        banco2.connect();
        Query query1 = new Query(banco2.conn);
        String sql = "SELECT dm.detalhesdolivro_id,c.titulo,dl.exemplar,dm.datadaentrega,dm.datadevolvida,m.codigo,dm.situacao"
                + " FROM detalhesmovimento dm,movimento m,cadlivros c,detalhesdolivro dl"
                + " WHERE dm.codmovimento=m.codigo"
                + " AND dl.cadlivros_id=c.codigo"
                + " AND dm.detalhesdolivro_id=dl.iddetalhesdolivro"
                + " AND NOT dm.situacao='R'"
                + " AND dm.codmovimento=" + modelLeitores.getRow(tblLeitores.getSelectedRow(), 4);
        query1.open(sql);
        //------------------------------
        String codigo;
        String titulo;
        String exemplar;
        String datadaentrega;
        String datadevolvida;
        String codlivro;
        String situacao;
        //------------------------------
        ArrayList vetor2 = new ArrayList();

        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        //Pega a data no formato do banco e armazena na variável
        java.sql.Date dtentrega = null;
        java.sql.Date dtdevolvida = null;
        while (query1.next()) {
            titulo = query1.fieldbyname("titulo");
            datadaentrega = query1.fieldbyname("datadaentrega");
            codigo = query1.fieldbyname("codigo");
            codlivro = query1.fieldbyname("detalhesdolivro_id");
            situacao = query1.fieldbyname("situacao");
            exemplar = query1.fieldbyname("exemplar");
            datadevolvida = query1.fieldbyname("datadevolvida");

            //Converte a string em DataJDBC no formato do banco de dados
            dtentrega = java.sql.Date.valueOf(datadaentrega);
            datadaentrega = df.format(dtentrega);
            if(datadevolvida!=null && datadevolvida.length()==10){
            dtdevolvida = java.sql.Date.valueOf(datadevolvida);
            datadevolvida = df.format(dtdevolvida);
            }else{
            datadevolvida="";
            }
            //Adiciona ao ArrayList os valores armazenados
            vetor2.add(new String[]{
                        new java.lang.String(new java.text.DecimalFormat("0000000000").format(new java.lang.Double(codlivro).doubleValue())),
                        titulo,
                        exemplar,
                        datadaentrega,
                        datadevolvida,
                        codigo,
                        situacao
                    });
        }
        //Desconecta
        banco2.disconnect();
        //=====================================================================
        String[] colunasObras = {"Código", "Título", "Exemplar", "Data da Devolução", "Devolvido"};

        boolean[] edicao = {false, false, false, false, false};
        modelObrasdoLeitor = new MkTableModel(vetor2, colunasObras, edicao);
        tblObrasdoLeitor.setModel(modelObrasdoLeitor);
//        sorter = new TableRowSorter<MkTableModel>(modelObrasdoLeitor);
//        tblObrasdoLeitor.setRowSorter(sorter);

        //"Código da Obra","Título,Local","Data da Devolução","Devolvido"
        tblObrasdoLeitor.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblObrasdoLeitor.getColumnModel().getColumn(1).setPreferredWidth(450);
        tblObrasdoLeitor.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblObrasdoLeitor.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblObrasdoLeitor.getColumnModel().getColumn(2).setPreferredWidth(100);

        if (tblObrasdoLeitor.getRowCount() == 0) {
            lblMensagem.setText("Nenhuma obra encontrada !");
        } else {
            lblMensagem.setText(tblObrasdoLeitor.getRowCount() + " obras encontradas ! ");
            tblObrasdoLeitor.requestFocus();
        }
    }

