            banco2.disconnect();
            //=====================================================================
            String[] colunasLeitor = {"Data", "Hora", "Código", "Nome do Leitor"};
            boolean[] edicao = {false, false, false, false};
            modelLeitores = new MkTableModel(vetor, colunasLeitor, edicao);
